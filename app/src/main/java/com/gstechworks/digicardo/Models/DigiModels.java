package com.gstechworks.digicardo.Models;

public class DigiModels {
    int img;
    String name;

    public DigiModels(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public String getName() {
        return name;
    }
}
