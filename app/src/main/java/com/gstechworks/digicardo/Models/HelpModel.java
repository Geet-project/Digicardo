package com.gstechworks.digicardo.Models;

public class HelpModel {
    String helpheading;
    String helpvalue;

    public HelpModel(String helpheading) {
        this.helpheading = helpheading;

    }

    public String getHelpheading() {
        return helpheading;
    }

    public String getHelpvalue() {
        return helpvalue;
    }
}
